public class Variable {


    public static void main(String[] args) {


        double cirkel1 = 1;

        double cirkel2 = 3;

        double cirkel3 = 5;

        double pi = 3.14;

        System.out.println("Cirkel1 radius er " + cirkel1*cirkel1*pi + " og omkredsen er " + 2*pi*cirkel1);

        System.out.println("Cirkel2 radius er " + cirkel2*cirkel2*pi + " og omkredsen er " + 2*pi*cirkel2);

        System.out.println("Cirkel3 radius er " + cirkel3*cirkel3*pi + " og omkredsen er " + 2*pi*cirkel3);


        double temp = 25;

        double toFahrenheit = 32 + 9/5*temp;

        System.out.println(temp + " grader celcius svarer til " + toFahrenheit + " grader fahrenheit");




    }




}
